# Tentukan Nilai Suhu
Celcius = int(input("Masukkan Nilai suhu celcius : "))

# Tentukan Fungsi Konversi dari Celcius ke Fahrenheit, Reamur dan Kelvin
Fahrenheit = (9/5 * Celcius) + 32
Reamur = 4/5 * Celcius
Kelvin = 273 + Celcius

# Hasil Output
print("Hasil Konversi Suhu dari Celcius ke Fahrenheit adalah : ", Fahrenheit, "F")
print("Hasil Konversi Suhu dari Celcius ke Reamur adalah : ", Reamur, "R")
print("Hasil Konversi Suhu dari Celcius ke Kelvin adalah : ", Kelvin, "K")